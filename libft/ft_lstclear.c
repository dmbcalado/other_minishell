#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*copy;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		copy = (*lst)-> next;
		ft_lstdelone(*lst, del);
		*lst = copy;
	}
}

/* Deletes and frees the given node and every
successor of that node, using the ft_lstdelone().
Finally, the pointer to the list must be set to
NULL. */